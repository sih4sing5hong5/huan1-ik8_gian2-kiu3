from 臺灣言語工具.解析整理.拆文分析器 import 拆文分析器

class 空白斷詞分析器:
	_分析器 = 拆文分析器()
	def 切出章物件(self,斷詞):
		章物件=self._分析器.建立章物件('')
		句字串陣列=self._分析器.拆章做句(斷詞)
		for 句字串 in 句字串陣列:
			章物件.內底句.append(
				self.切出句物件(句字串)
				)
		return 章物件
	def 切出句物件(self,斷詞):
		組物件=self._分析器.建立組物件('')
		for 詞 in 斷詞.split():
			詞物件=self._分析器.建立詞物件(詞)
			組物件.內底詞.append(詞物件)
		集物件=self._分析器.建立集物件('')
		集物件.內底組.append(組物件)
		句物件=self._分析器.建立句物件('')
		句物件.內底集.append(集物件)
		return 句物件