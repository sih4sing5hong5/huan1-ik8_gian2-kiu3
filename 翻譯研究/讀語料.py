
class 讀語料:
	def 產生對應表(self, 來源詞, 目標詞, 機率表):
		來源詞檔案 = open(來源詞)
		目標詞檔案 = open(目標詞)
		機率表檔案 = open(機率表)
		來源對應表 = {}
		for 一逝 in 來源詞檔案:
			編號, 詞, 出現次數 = 一逝.split()
			來源對應表[編號] = 詞
		來源詞檔案.close()
		目標對應表 = {}
		for 一逝 in 目標詞檔案:
			編號, 詞, 出現次數 = 一逝.split()
			目標對應表[編號] = 詞
		目標詞檔案.close()
		對應表 = {}
		for 一逝 in 機率表檔案:
			來源, 目標, 機率 = 一逝.split()
			if 來源 not in 對應表:
				對應表[來源] = []
			對應表[來源].append((目標, 機率))
# 			print('對應表[來源]',來源,對應表[來源])
		機率表檔案.close()
# 		print(來源對應表)
		print(str(對應表)[:1000])
		
if __name__ == '__main__':
	語料 = 讀語料()
